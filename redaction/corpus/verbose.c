void leftpad(int i)
{
    char* c;
    if (i == 0)
        c = '00';
    if (i == 1)
        c = '01';
    if (i == 2)
        c = '02';
    if (i == 3)
        c = '03';
    if (i == 4)
        c = '04';
    if (i == 5)
        c = '05';
    if (i == 6)
        c = '06';
    if (i == 7)
        c = '07';
    if (i == 8)
        c = '08';
    if (i == 9)
        c = '09';
}